namespace XUnitProject
{
    public class Rest
    {
         public int restInt (int num1, int num2)
       {
           int total;
           total=num1 - num2;
           return total;

       }
        public double restDouble (double num1, double num2)
       {
           double total;
           total=num1 - num2;
           return total;

       }
        
    }
}